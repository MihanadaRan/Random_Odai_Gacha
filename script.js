const bg = document.querySelector(".bg");
const select = document.querySelector(".select");
const wordEl = document.getElementById("word");
const card = document.querySelector(".card");

// ページ読み込み時に初期ズーム＆ぼかし
window.addEventListener("DOMContentLoaded", () => {
  bg.classList.add("blur-initial", "zoom");
  // 初期ロード後もズームは残す → transform固定
});

// 背景切替＆ズーム
function showBg(type) {
  // 背景切替
  bg.classList.remove("a", "b");
  bg.classList.add(type);

  // ズームアニメを再実行するため一旦 zoom を削除
  bg.classList.remove("zoom");
  void bg.offsetWidth; // 再描画を強制

  // ズームアニメーション付与
  bg.classList.add("zoom");

  // UI切り替え
  select.style.display = "none";
  card.style.display = "flex";

  requestAnimationFrame(() => {
    card.classList.add("show");
  });
}

document.getElementById("bg1").onclick = () => showBg("a");
document.getElementById("bg2").onclick = () => showBg("b");

